    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JFileChooser fileChooser = new JFileChooser();
        int retorno = fileChooser.showOpenDialog(this);
        if (retorno == JFileChooser.APPROVE_OPTION) {
            arquivo = fileChooser.getSelectedFile();
            arquivoLoc.setText(arquivo.getAbsolutePath());

        } else {
            arquivo = null;
            arquivoLoc.setText("");
        }

        try {
            String delimitadorStr = delimitador.getText();

            //Abrir o arquivo
            BufferedReader br;
            br = new BufferedReader(new FileReader(arquivo));

            //Definir header da tabela
            String linha = br.readLine();
            StringTokenizer tokens = new StringTokenizer(linha, delimitadorStr);
            String header[] = new String[tokens.countTokens()];
            for (int i = 0; i < header.length; i++) {
                header[i] = tokens.nextToken();
            }
            DefaultTableModel modelo = new DefaultTableModel(header, 0);

            //Adicionar o resto das linhas
            DecimalFormat df = new DecimalFormat("##.#");

            linha = br.readLine();
            while (linha != null) {
                tokens = new StringTokenizer(linha, delimitadorStr);
                String row[] = new String[tokens.countTokens()];
                for (int i = 0; i < row.length; i++) {
                    row[i] = tokens.nextToken().replace(',', '.');
                }
                modelo.addRow(row);
                linha = br.readLine();
            }
            tabela.setModel(modelo);

            //Total de linhas
            int Total = tabela.getRowCount();
            String totalAmostra = String.valueOf(Total);
            amostraTotal.setText(totalAmostra);

            //Ativar escolher Coluna e botao Calcular
            colunaDesejada.setEditable(true);
            botaoCalcular.setEnabled(true);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(Color.LIGHT_GRAY);

        for (int i = 0; i < tabela.getModel().getColumnCount(); i++) {
            tabela.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
        }

        adicionarAmostra.setEnabled(false);
        addValor.setEnabled(false);

    }//GEN-LAST:event_jButton1ActionPerformed

