    private void botaoCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularActionPerformed

        try {
            double valores[] = new double[tabela.getRowCount()];
            int colunaEscolhida = Integer.parseInt(colunaDesejada.getText());
            int colunaEscolhida1 = colunaEscolhida - 1;

            //Criar vetor com os valores da JTable
            for (int i = 0; i < valores.length; i++) {
                valores[i] = Double.parseDouble(tabela.getValueAt(i, colunaEscolhida1).toString());

            }

            //Calcular media
            mediaInt = calculos.getMedia(valores);
            mediaFinal = String.valueOf(mediaInt);                   //Media String

            //Ordenar vetor
            double aux;
            for (int i = 0; i < valores.length; i++) {
                for (int j = i + 1; j < valores.length; j++) {
                    if (valores[j] < valores[i]) {
                        aux = valores[i];
                        valores[i] = valores[j];
                        valores[j] = aux;
                    }
                }
            }

            //Calcular mediana
            double medianaD = calculos.getMediana(valores);
            String medianaString = String.valueOf(medianaD);                //Mediana String

            //Calcular Moda
            double modaD = calculos.getModa(valores);
            String modaString = String.valueOf(modaD);                      //Moda String

            //Calcular Variancia
            double varianciaD = calculos.getVariancia(valores);
            String varianciaString = String.valueOf(varianciaD);            //Variancia String

            //Calcular Desvio Padrão 
            double desvioPadraoD = calculos.getDesvioPadrao(valores);
            String desvioPadraoString = String.valueOf(desvioPadraoD);     //Desvio Padrao String

            //Calcular Coeficiente de Variação
            double coeficienteDeVariacaoD = calculos.getCoeficienteVariacao(valores);
            String coeficienteDeVariacaoString = String.valueOf(coeficienteDeVariacaoD);     //Desvio Padrao String

            DecimalFormat df = new DecimalFormat("##.#");

            //Definir caixas de texto na interface
            media.setText(df.format(mediaInt));
            mediana.setText(df.format(medianaD));
            moda.setText(df.format(modaD));
            variancia.setText(df.format(varianciaD));
            desvioPadrao.setText(df.format(desvioPadraoD));
            coeficienteDeVariacao.setText(df.format(coeficienteDeVariacaoD));
            botaoResultados.setEnabled(true);

            //Infos dos gráficos
            v = new double[valores.length];
            f = new int[valores.length];
            int j = 0;
            double ant = valores[0];
            int cont = 0;
            for (int i = 0; i < valores.length; i++) {
                if (ant == valores[i]) {
                    cont++;
                } else {
                    ant = valores[i];
                    j++;
                    cont = 1;
                }
                f[j] = cont;
                v[j] = valores[i];
            }

    }
    catch (NumberFormatException e

    
        ) {
            JOptionPane.showMessageDialog(this, "Coluna Inválida", "ERRO!", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_botaoCalcularActionPerformed

